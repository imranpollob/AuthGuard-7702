#!/usr/bin/env python3
"""Phase 6A — reproducibility artifact manifest + hash ledger.

Enumerates every v2 script, result, protocol, audit, and the frozen inputs the artifact needs,
emits SHA-256 for each, and writes deterministic reconstruction instructions. Does not copy
third-party corpora (licensing); instead records hashes + row counts + fetch/reconstruct steps.
"""
import hashlib
import json
import os

ROOT = os.path.abspath(os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "..", ".."))
RV2 = os.path.join(ROOT, "revision_v2")
OUT = os.path.join(RV2, "artifact")


def sha256(path):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for c in iter(lambda: f.read(1 << 20), b""):
            h.update(c)
    return h.hexdigest()


def walk(rel):
    base = os.path.join(ROOT, rel)
    out = {}
    if os.path.isfile(base):
        out[rel] = sha256(base)
        return out
    for dp, _dn, fns in os.walk(base):
        for fn in sorted(fns):
            if (fn.endswith((".pyc", ".log")) or "__pycache__" in dp or fn == ".DS_Store" or
                    fn == "REVIEWER_KEY_do_not_distribute.csv"):
                continue
            p = os.path.join(dp, fn)
            out[os.path.relpath(p, ROOT)] = sha256(p)
    return out


def main():
    os.makedirs(OUT, exist_ok=True)
    ledger = {}
    for rel in ["revision_v2/experiments", "revision_v2/protocols", "revision_v2/results",
                "revision_v2/audits", "revision_v2/reports", "revision_v2/planning",
                "revision_v2/artifact/label_audit", "revision_v2/manuscript"]:
        ledger.update(walk(rel))
    for fn in sorted(os.listdir(RV2)):
        if fn.startswith("final_") and fn.endswith(".md"):
            ledger.update(walk(os.path.join("revision_v2", fn)))
    # frozen inputs (hash only, not redistributed if restricted)
    frozen_inputs = {}
    for rel in ["capability_dataset.csv", "family_assignment_frozen.csv",
                "paper_build/data_hygiene/task_aligned_dataset_v1.csv"]:
        p = os.path.join(ROOT, rel)
        if os.path.exists(p):
            import pandas as pd
            try:
                n = len(pd.read_csv(p))
            except Exception:
                n = None
            frozen_inputs[rel] = dict(sha256=sha256(p), rows=n,
                                      redistribute="hash+reconstruct only (USENIX-derived)")
    for rel in ["paper_build/overleaf/main.tex", "paper_build/overleaf/references.bib",
                "paper_build/overleaf/figures/random_vs_family.pdf"]:
        p = os.path.join(ROOT, rel)
        if os.path.exists(p):
            frozen_inputs[rel] = dict(sha256=sha256(p), rows=None,
                                      redistribute="repository manuscript dependency")
    protocol_entries = []
    protocol_ledger = os.path.join(RV2, "protocols", "protocols.sha256")
    with open(protocol_ledger) as f:
        for line in f:
            expected, rel = line.strip().split(maxsplit=1)
            actual = sha256(os.path.join(ROOT, rel))
            protocol_entries.append(dict(path=rel, expected=expected, actual=actual,
                                         match=expected == actual))
    payload = dict(
        artifact="AuthGuard-7702 Revision v2",
        n_files=len(ledger),
        files=ledger,
        frozen_inputs=frozen_inputs,
        protocol_integrity=dict(
            status=("PASS" if all(x["match"] for x in protocol_entries)
                    else "FAIL_STALE_LEDGER_ENTRY"),
            entries=protocol_entries,
            note=("The donor-isolation v1.1 amendment is committed before donor-isolated "
                  "results, but protocols.sha256 retains its v1.0 hash; immutable artifacts "
                  "were not rewritten during finalization.")),
        reconstruction=[
            "1. Environment: revision_v2/audits/requirements.txt + pip_freeze.txt (Python 3.13).",
            "2. Verify frozen inputs by SHA-256 (frozen_inputs above); task_aligned_dataset_v1.csv "
            "is regenerated by paper_build/data_hygiene/task_alignment.py from capability_dataset.csv.",
            "3. Run revision_v2/experiments/common/frozen.py build (once) then verify.",
            "4. Run experiments in order: gdet_v2, gmut_v2, gadv_v2, "
            "uncertainty/bootstrap_{gdet,gadv}, secondary_controls, weighting, baselines "
            "(including first-STOP investigation), bootstrap_baselines, family_sensitivity, "
            "gateA plus gateA_verdict, gateB, operational, then manuscript table generation "
            "and integration. Each writes machine-readable "
            "outputs; experiment manifests record hashes and versions.",
            "5. Donor provenance ledgers: revision_v2/results/*/donor_ledger_*.csv(.gz).",
            "6. Label-audit package: revision_v2/artifact/label_audit/ (blinded forms + agreement.py).",
            "7. Run artifact/sanitize_manifests.py, rebuild this inventory, run "
            "artifact/audit_anonymity.py, and verify phase manifests.",
        ],
        licensing_note=("Bytecode corpus is USENIX-artifact-derived; on-chain bytecode is public "
                        "but the labeled compilation may carry artifact terms. Redistribute the "
                        "processing/analysis code and hashes; reconstruct the corpus via the "
                        "task-alignment script + hashes if redistribution is restricted."),
        anonymity_note=("Paths are repository-relative. The human-reviewer key is excluded "
                        "from the anonymous artifact and must be retained by the audit chair."),
    )
    with open(os.path.join(OUT, "artifact_manifest.json"), "w") as f:
        json.dump(payload, f, indent=2)
    print(f"[artifact] {len(ledger)} files hashed -> {OUT}/artifact_manifest.json")


if __name__ == "__main__":
    main()
